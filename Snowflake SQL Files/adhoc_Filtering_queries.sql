-- Query 14: Show the top 3 brands based on there sales in the last 1 year

SELECT 
    P.BRAND,
    SUM(O.TOTALAMOUNT) AS TOTAL_SALES_IN_LAST_YEAR
FROM FACTORDERS O
JOIN DIMDATE D
ON O.DATEID = D.DATEID
JOIN DIMPRODUCT P
ON O.PRODUCTID = P.PRODUCTID
WHERE D.DATE >= DATEADD(YEAR, -1, CURRENT_DATE)
GROUP BY P.BRAND
ORDER BY SUM(O.TOTALAMOUNT) DESC
LIMIT 3;

-- Query 15: If the discount amount and the shipping cost was made static at 5% and 8% respectively, will the sum of new total amount be greater than the total amount we have

WITH UPDATED_TOTAL_AMOUNT AS (
    SELECT
        (ORDERAMOUNT - 0.05*ORDERAMOUNT + 0.08*ORDERAMOUNT) AS NEW_TOTAL_AMOUNT
    FROM FACTORDERS
)
SELECT 
    CASE 
        WHEN (SUM(O1.NEW_TOTAL_AMOUNT) > SUM(O.TOTALAMOUNT)) 
        THEN 'YES'
    ELSE 'NO'
    END
FROM FACTORDERS O, UPDATED_TOTAL_AMOUNT O1;

