-- Query 17: Show the region & category wise total amount for the last 6 months

SELECT
    S.REGION,
    P.CATEGORY,
    SUM(TOTALAMOUNT) AS TOTAL_SALES
FROM FACTORDERS O
JOIN DIMDATE D
ON O.DATEID = D.DATEID
JOIN DIMPRODUCT P
ON P.PRODUCTID = O.PRODUCTID
JOIN DIMSTORE S
ON O.STOREID = S.STOREID
WHERE D.DATE >= DATEADD(MONTH, -6, CURRENT_DATE)
GROUP BY 
    S.REGION,
    P.CATEGORY;

-- Query 18: Show the top 5 products based on quantity ordered in the last 3 years

SELECT 
    P.PRODUCTNAME,
    SUM(O.QUANTITYORDERED) AS QUANTITY_ORDERED
FROM FACTORDERS O
JOIN DIMDATE D
ON O.DATEID = D.DATEID
JOIN DIMPRODUCT P
ON P.PRODUCTID = O.PRODUCTID
WHERE D.DATE >= DATEADD(YEAR, -3, CURRENT_DATE)
GROUP BY P.PRODUCTNAME
ORDER BY SUM(O.QUANTITYORDERED) DESC
LIMIT 5;

-- Query 19: List the total amount for each loyalty program tier since year 2023

SELECT 
    L.PROGRAMTIER,
    SUM(O.TOTALAMOUNT)
FROM FACTORDERS O
JOIN DIMDATE D
ON O.DATEID = D.DATEID
JOIN DIMCUSTOMER C
ON C.CUSTOMERID = O.CUSTOMERID
JOIN DIMLOYALTYPROGRAM L
ON C.LOYALTYPROGRAMID = L.LOYALTYPROGRAMID
WHERE D.YEAR >= 2023
GROUP BY 
    L.PROGRAMTIER;

-- Query 20: Calculate the revenue generated by each store manager in June 2024

SELECT 
    S.MANAGERNAME,
    SUM(O.TOTALAMOUNT)
FROM FACTORDERS O
JOIN DIMDATE D
ON O.DATEID = D.DATEID
JOIN DIMSTORE S
ON O.STOREID = S.STOREID
WHERE D.YEAR = 2024 AND D.MONTH = 6
GROUP BY S.MANAGERNAME;

-- Query 21: List the average order amount per store, along with the store name and type for the year 2024.

SELECT 
    S.STORENAME,
    S.STORETYPE,
    AVG(O.ORDERAMOUNT)
FROM FACTORDERS O
JOIN DIMDATE D
ON O.DATEID = D.DATEID
JOIN DIMSTORE S
ON O.STOREID = S.STOREID
WHERE D.YEAR = 2024
GROUP BY 
    S.STORENAME,
    S.STORETYPE;

