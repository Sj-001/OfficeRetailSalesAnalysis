-- Query 7: List the store that was opened most recently along with its sales
WITH STORE_RANK AS (
    SELECT 
        STOREID,
        STOREOPENINGDATE,
        ROW_NUMBER() OVER (ORDER BY STOREOPENINGDATE DESC) AS FINAL_RANK 
    FROM DIMSTORE
),
MOST_RECENT_STORE AS (
    SELECT STOREID FROM STORE_RANK WHERE FINAL_RANK = 1
),
STORE_AMOUNT AS (
    SELECT 
    O.STOREID,
    SUM(TOTALAMOUNT) AS TOTAL_SALES
    FROM FACTORDERS O
    JOIN MOST_RECENT_STORE S 
    ON O.STOREID = S.STOREID
    GROUP BY O.STOREID
)
SELECT 
    S.*, 
    A.TOTAL_SALES 
FROM DIMSTORE S
JOIN STORE_AMOUNT A
ON A.STOREID = S.STOREID;

-- Query 8: Find customers who have ordered product from more than 3 categories in the last 6 months

-- All the orders from past 6 months
SELECT 
    * 
FROM FACTORDERS F
JOIN DIMDATE D
ON F.DATEID = D.DATEID
WHERE D.DATE >= DATEADD(MONTH, -6, CURRENT_DATE);

-- selecting distinct customers with categories they ordered

SELECT 
    O.CUSTOMERID,
    COUNT(DISTINCT P.CATEGORY)
FROM FACTORDERS O
JOIN DIMDATE D
ON O.DATEID = D.DATEID
JOIN DIMPRODUCT P
ON O.PRODUCTID = P.PRODUCTID
WHERE D.DATE >= DATEADD(MONTH, -6, CURRENT_DATE)
GROUP BY 
    O.CUSTOMERID
HAVING COUNT(DISTINCT P.CATEGORY) > 3;

-- Query 9: Get the monthly total sales for the current year

SELECT 
    D.MONTH,
    SUM(O.TOTALAMOUNT) AS MONTHLY_SALES_OF_CURRENT_YEAR
FROM DIMDATE D
JOIN FACTORDERS O
ON O.DATEID = D.DATEID
WHERE D.YEAR = EXTRACT(YEAR FROM CURRENT_DATE)
GROUP BY MONTH
ORDER BY MONTH;

-- Query 10: Find the highest discount given on any order in the last 1 year

SELECT 
    MAX(O.DISCOUNTAMOUNT)
FROM FACTORDERS O
JOIN DIMDATE D
ON O.DATEID = D.DATEID
WHERE D.DATE >= DATEADD(YEAR, -1, CURRENT_DATE);

